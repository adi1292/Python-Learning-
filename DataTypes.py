#Data Types in Python 
#1.Integer Data Type
#2.Float Data Type
#3.String Data Type
#4.Boolean Data Type


#1.Integer Data Type
x = 10;
y = 20;
print(x);
print(y);
#2.Float Data Type
a = 5.5;
b = 10.5;
print(a);   
print(b);

#3.String Data Type
name = "John";
city = "Pune";
print(name);
print(city);

#4.Boolean Data Type
is_student = True;
is_graduate = False;
print(is_student);
print(is_graduate);

#Difference between number and string data types:
#Number data types are used to store numeric values, while string data types are used to store text values.
age = 24; #Number data type
age="24"; #String data type
print(age);
print(age);


#Checking The Data Type of a Variable
